///////////////////////////////////////////////////////////////////////////
/////////////////////////////// pretty_json ///////////////////////////////
///////////////////////////////////////////////////////////////////////////
export function pretty_json(in_json) {

    // variables & constants
    const white_space_count = 4
    
    // actual logic to prettify the json
    const out_json = JSON.stringify(in_json, null, white_space_count).toString()
    return out_json
} 


export const log_lvl_map = new Map (
    [
        [1, "ERROR"],
        [2, "WARNING"],
        [3, "DEBUG"]
    ]
)
///////////////////////////////////////////////////////////////////////////
/////////////////////////////// err_log_msg ///////////////////////////////
///////////////////////////////////////////////////////////////////////////
export function err_log_msg(log_lvl, msg) {
    console.log("\n>> ",log_lvl_map.get(log_lvl), "|", Date(Date.now().toLocaleString()), "|")
    console.log(msg)
}


///////////////////////////////////////////////////////////////////////////
////////////////////////////// err_log_json ///////////////////////////////
///////////////////////////////////////////////////////////////////////////
export function err_log_json(log_lvl, msg, json_data) {
    console.log("\n>> ",log_lvl_map.get(log_lvl), "|", Date(Date.now().toLocaleString()), "|")
    console.log(msg)
    console.log(pretty_json(json_data))
}


